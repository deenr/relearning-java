public class Variables {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println("This is a boolean with value " + a);

        byte b = 12;
        System.out.println("This is a byte with value " + b);

        short c = 10000;
        System.out.println("This is a short with value " + c);

        int d = 1234567890;
        System.out.println("This is a int with value " + d);

        long e = 780000000000000000L;
        System.out.println("This is a long with value " + e);

        float f = 0.12345f;
        System.out.println("This is a float with value " + f);

        double g = 0.123456789012345;
        System.out.println("This is a double with value " + g);

        char h = 'h';
        System.out.println("This is a char with value " + h);

        String i = "Hello world";
        System.out.println("This is a String with value " + i);
    }
}